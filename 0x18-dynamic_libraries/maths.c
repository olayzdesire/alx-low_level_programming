#include "desire.h"
/**
 * add, sub, mul, mod, div - functions in the 100-operations.so library
 *
 */

int add(int a, int b)
{
	return (a + b);
}

int sub(int a, int b)
{
	return (a - b);
}

int mul(int a, int b)
{
	return (a * b);
}

int mod(int a, int b)
{
	return (a % b);
}

int div(int a, int b)
{
	return (a / b);
}
